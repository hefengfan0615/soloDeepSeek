# ============== DeepSeek API 客户端 ==============
class DeepSeekClient:
    BASE = "https://chat.deepseek.com"
    UA = (
        "Mozilla/5.0 (X11; Linux x86_64) AppleWebKit/537.36 "
        "(KHTML, like Gecko) Chrome/126.0.0.0 Safari/537.36"
    )

    def __init__(self, mobile: str, password: str, token_file: str):
        self.mobile = mobile
        self.password = password
        self.token_file = Path(token_file)
        self.token: str | None = None
        self._client: httpx.Client | None = None

        # 初始化 PoW WASM (懒加载)
        self._engine = wasmtime.Engine()
        self._store = wasmtime.Store(self._engine)
        self._module = wasmtime.Module(self._engine, base64.b64decode(WASM_B64))
        self._instance = wasmtime.Instance(self._store, self._module, [])
        self._exp = self._instance.exports(self._store)
        self._memory = self._exp["memory"]
        self._realloc = self._exp["__wbindgen_export_0"]
        self._add_to_sp = self._exp["__wbindgen_add_to_stack_pointer"]
        self._wasm_solve = self._exp["wasm_solve"]

    # ---------- 通用请求 ----------
    def _common_headers(self) -> dict:
        return {
            "User-Agent": self.UA,
            "x-app-version": "2.0.0",
            "x-client-locale": "en_US",
            "x-client-platform": "web",
            "x-client-version": "2.0.0",
            "x-client-timezone-offset": "0",
            "Accept": "application/json",
            "Content-Type": "application/json",
            "Referer": f"{self.BASE}/",
            "Origin": self.BASE,
        }

    def _client_with_auth(self) -> httpx.Client:
        if self._client is None:
            headers = self._common_headers()
            if self.token:
                headers["Authorization"] = f"Bearer {self.token}"
            self._client = httpx.Client(headers=headers, timeout=30.0)
        return self._client

    def close(self):
        if self._client is not None:
            self._client.close()
            self._client = None

    # ---------- Token 持久化 ----------
    def _save_token(self):
        if not self.token:
            return
        self.token_file.write_text(
            json.dumps(
                {"token": self.token, "saved_at": int(time.time()), "mobile": self.mobile},
                ensure_ascii=False,
            )
        )

    def _load_token(self) -> str | None:
        if not self.token_file.exists():
            return None
        try:
            data = json.loads(self.token_file.read_text())
            return data.get("token")
        except Exception:
            return None

    # ---------- PoW 计算 (DeepSeekHashV1) ----------
    def _write_str_to_wasm(self, s: str) -> tuple[int, int]:
        bs = s.encode("utf-8")
        ptr = self._realloc(self._store, len(bs), 1)
        addr = ctypes.cast(self._memory.data_ptr(self._store), ctypes.c_void_p).value
        ctypes.memmove(addr + ptr, bs, len(bs))
        return ptr, len(bs)

    def solve_pow(self, challenge: str, salt: str, expire_at: int, difficulty: int) -> int:
        """
        调用 DeepSeekHashV1 WASM 求解 PoW。
        算法:prefix = f"{salt}_{expire_at}_"
              answer  = wasm_solve(challenge, prefix, difficulty)
        """
        prefix = f"{salt}_{expire_at}_"
        c_ptr, c_len = self._write_str_to_wasm(challenge)
        p_ptr, p_len = self._write_str_to_wasm(prefix)
        sp = self._add_to_sp(self._store, -16)
        self._wasm_solve(
            self._store, sp, c_ptr, c_len, p_ptr, p_len, float(difficulty)
        )
        addr = ctypes.cast(self._memory.data_ptr(self._store), ctypes.c_void_p).value
        raw = bytes((ctypes.c_uint8 * 16).from_address(addr + sp))
        self._add_to_sp(self._store, 16)
        status = struct.unpack("<i", raw[:4])[0]
        answer = struct.unpack("<d", raw[8:16])[0]
        if status != 1:
            raise RuntimeError(f"PoW 求解失败: status={status}, answer={answer}")
        return int(answer)

    # ---------- 登录 ----------
    def login(self) -> str:
        device_id = base64.b64encode(uuid.uuid4().bytes + uuid.uuid4().bytes).decode()
        body = {
            "email": "",
            "mobile": self.mobile,
            "password": self.password,
            "area_code": "+86",
            "device_id": device_id,
            "os": "web",
        }
        # 登录请求不能带旧 token
        client = httpx.Client(headers=self._common_headers(), timeout=30.0)
        try:
            r = client.post(f"{self.BASE}/api/v0/users/login", json=body)
        finally:
            client.close()
        data = r.json()
        if data.get("code") != 0:
            raise RuntimeError(f"登录失败: {data}")
        self.token = data["data"]["biz_data"]["user"]["token"]
        self._save_token()
        return self.token

    def current_user(self) -> dict | None:
        """验证 token 是否有效;返回 user dict,无效时返回 None"""
        if not self.token:
            return None
        client = self._client_with_auth()
        r = client.get(f"{self.BASE}/api/v0/users/current")
        if r.status_code != 200:
            return None
        data = r.json()
        if data.get("code") != 0:
            return None
        return data["data"]["biz_data"]

    def ensure_token(self) -> str:
        """启动时调用:优先用磁盘 token,失效才重新登录。"""
        if self.token is None:
            self.token = self._load_token()
        if self.token:
            user = self.current_user()
            if user is not None:
                return self.token
        print("[*] 本地 token 失效或不存在,自动重新登录...")
        return self.login()

    # ---------- 业务接口 ----------
    def list_models(self) -> list[dict]:
        client = self._client_with_auth()
        did = str(uuid.uuid4())
        r = client.get(
            f"{self.BASE}/api/v0/client/settings",
            params={"did": did, "scope": "model"},
        )
        data = r.json()
        if data.get("code") != 0:
            raise RuntimeError(f"获取模型失败: {data}")
        return data["data"]["biz_data"]["settings"]["model_configs"]["value"]

    def create_session(self) -> str:
        client = self._client_with_auth()
        r = client.post(f"{self.BASE}/api/v0/chat_session/create", json={})
        data = r.json()
        if data.get("code") != 0:
            raise RuntimeError(f"创建会话失败: {data}")
        return data["data"]["biz_data"]["chat_session"]["id"]

    def delete_session(self, session_id: str):
        client = self._client_with_auth()
        r = client.post(
            f"{self.BASE}/api/v0/chat_session/delete",
            json={"chat_session_id": session_id},
        )
        data = r.json()
        if data.get("code") != 0:
            raise RuntimeError(f"删除会话失败: {data}")

    def update_user_settings(
        self,
        search_enabled: bool | None = None,
        thinking_enabled: bool | None = None,
    ):
        """更新用户级默认设置(联网搜索 / 深度思考)。"""
        client = self._client_with_auth()
        payload: dict = {}
        if search_enabled is not None:
            payload["search_enabled"] = search_enabled
        if thinking_enabled is not None:
            payload["thinking_enabled"] = thinking_enabled
        if not payload:
            return
        # DeepSeek 使用 PATCH 类语义: 走 /api/v0/users/update_settings
        r = client.post(
            f"{self.BASE}/api/v0/users/update_settings", json=payload
        )
        data = r.json()
        if data.get("code") != 0:
            # 兼容部分版本没有 update_settings 的情况,忽略错误
            print(f"[!] update_settings 返回非 0 (忽略): {data.get('msg')}")

    def get_pow_challenge(self, target_path: str = "/api/v0/chat/completion") -> dict:
        client = self._client_with_auth()
        r = client.post(
            f"{self.BASE}/api/v0/chat/create_pow_challenge",
            json={"target_path": target_path},
        )
        data = r.json()
        if data.get("code") != 0:
            raise RuntimeError(f"获取 PoW 失败: {data}")
        return data["data"]["biz_data"]["challenge"]

    def chat(
        self,
        session_id: str,
        prompt: str,
        model_type: str = "expert",
        search_enabled: bool = True,
        thinking_enabled: bool = True,
        parent_message_id: str | None = None,
    ) -> dict:
        """
        发起一次 chat completion 请求(SSE 流),返回最后一个 message 的 dict。
        model_type: 'default' (Instant) | 'expert' (深度思考)
        """
        client = self._client_with_auth()
        ch = self.get_pow_challenge("/api/v0/chat/completion")
        answer = self.solve_pow(
            ch["challenge"], ch["salt"], ch["expire_at"], ch["difficulty"]
        )
        pow_response = {
            "algorithm": ch["algorithm"],
            "challenge": ch["challenge"],
            "salt": ch["salt"],
            "answer": answer,
            "signature": ch["signature"],
            "target_path": ch["target_path"],
        }
        pow_header = base64.b64encode(
            json.dumps(pow_response).encode()
        ).decode()
        body = {
            "chat_session_id": session_id,
            "parent_message_id": parent_message_id,
            "model_type": model_type,
            "prompt": prompt,
            "ref_file_ids": [],
            "thinking_enabled": thinking_enabled,
            "search_enabled": search_enabled,
            "action": None,
            "preempt": False,
        }
        headers = {**client.headers, "x-ds-pow-response": pow_header}
        with httpx.Client(headers=headers, timeout=180.0) as stream_client:
            with stream_client.stream(
                "POST",
                f"{self.BASE}/api/v0/chat/completion",
                json=body,
            ) as resp:
                if resp.status_code != 200:
                    body_text = resp.read().decode("utf-8", "ignore")
                    raise RuntimeError(
                        f"chat/completion 失败: {resp.status_code} {body_text[:300]}"
                    )
                return self._consume_sse(resp)

    # =====================================================================
    # ================= OpenAI 兼容接口 ===================================
    # =====================================================================
    def openai_chat(
        self,
        messages: list[dict],
        model: str = "default",
        stream: bool = False,
        search_enabled: bool = True,
        thinking_enabled: bool = True,
        temperature: float | None = None,
        max_tokens: int | None = None,
    ):
        """
        OpenAI 兼容 chat.completion 接口。
        - 输入: messages=[{"role":..., "content":...}, ...]
        - 输出:
            - stream=False: 返回 OpenAI chat.completion dict
            - stream=True:  生成器, yield OpenAI chat.completion.chunk dict
        思考放在 delta.reasoning_content / message.reasoning_content 字段。
        搜索结果不单独返回,模型已将其融合到正文 content 中。
        """
        # 1) 拼成单条 prompt
        prompt_parts: list[str] = []
        system_parts: list[str] = []
        for m in messages:
            role = m.get("role")
            content = m.get("content") or ""
            if role == "system":
                system_parts.append(content)
            elif role == "user":
                prompt_parts.append(f"User: {content}")
            elif role == "assistant":
                prompt_parts.append(f"Assistant: {content}")
        if system_parts:
            prompt_parts.insert(0, "System: " + "\n".join(system_parts))
        # DeepSeek 只接受最后一条 user 输入, 拼好后整体作为 prompt
        prompt = "\n\n".join(prompt_parts) + "\n\nAssistant:"

        # 2) 准备会话
        token = self.ensure_token()
        session_id = self.create_session()
        try:
            # 3) 流式
            if stream:
                return self._openai_stream(
                    session_id=session_id,
                    prompt=prompt,
                    model=model,
                    search_enabled=search_enabled,
                    thinking_enabled=thinking_enabled,
                )
            # 4) 非流式
            return self._openai_collect(
                session_id=session_id,
                prompt=prompt,
                model=model,
                search_enabled=search_enabled,
                thinking_enabled=thinking_enabled,
            )
        finally:
            try:
                self.delete_session(session_id)
            except Exception:
                pass

    def _openai_stream(
        self,
        session_id: str,
        prompt: str,
        model: str,
        search_enabled: bool,
        thinking_enabled: bool,
    ):
        """生成器:yield OpenAI 格式的 chunk dict"""
        import time as _time
        import uuid as _uuid
        chat_id = f"chatcmpl-{_uuid.uuid4().hex[:24]}"
        created = int(_time.time())
        oa_model = model
        # 首个 chunk: role
        yield {
            "id": chat_id,
            "object": "chat.completion.chunk",
            "created": created,
            "model": oa_model,
            "choices": [
                {"index": 0, "delta": {"role": "assistant"}, "finish_reason": None}
            ],
        }
        for ev in self._iter_sse(
            session_id=session_id,
            prompt=prompt,
            model_type=model,
            search_enabled=search_enabled,
            thinking_enabled=thinking_enabled,
        ):
            t = ev.get("type")
            if t == "thinking":
                yield {
                    "id": chat_id,
                    "object": "chat.completion.chunk",
                    "created": created,
                    "model": oa_model,
                    "choices": [
                        {
                            "index": 0,
                            "delta": {"reasoning_content": ev["delta"]},
                            "finish_reason": None,
                        }
                    ],
                }
            elif t == "text":
                yield {
                    "id": chat_id,
                    "object": "chat.completion.chunk",
                    "created": created,
                    "model": oa_model,
                    "choices": [
                        {
                            "index": 0,
                            "delta": {"content": ev["delta"]},
                            "finish_reason": None,
                        }
                    ],
                }
        # 末尾 chunk: finish_reason
        yield {
            "id": chat_id,
            "object": "chat.completion.chunk",
            "created": created,
            "model": oa_model,
            "choices": [{"index": 0, "delta": {}, "finish_reason": "stop"}],
        }

    def _openai_collect(
        self,
        session_id: str,
        prompt: str,
        model: str,
        search_enabled: bool,
        thinking_enabled: bool,
    ) -> dict:
        """非流式:收集完所有 chunk,返回 OpenAI chat.completion dict"""
        import time as _time
        import uuid as _uuid
        text_parts: list[str] = []
        think_parts: list[str] = []
        for chunk in self._openai_stream(
            session_id=session_id,
            prompt=prompt,
            model=model,
            search_enabled=search_enabled,
            thinking_enabled=thinking_enabled,
        ):
            for choice in chunk.get("choices") or []:
                delta = choice.get("delta") or {}
                if "content" in delta:
                    text_parts.append(delta["content"])
                if "reasoning_content" in delta:
                    think_parts.append(delta["reasoning_content"])
        chat_id = f"chatcmpl-{_uuid.uuid4().hex[:24]}"
        created = int(_time.time())
        text = "".join(text_parts)
        think = "".join(think_parts)
        message: dict = {"role": "assistant", "content": text}
        if think:
            message["reasoning_content"] = think
        return {
            "id": chat_id,
            "object": "chat.completion",
            "created": created,
            "model": model,
            "choices": [
                {"index": 0, "message": message, "finish_reason": "stop"}
            ],
            "usage": {
                "prompt_tokens": 0,
                "completion_tokens": len(text) + len(think),
                "total_tokens": len(text) + len(think),
            },
        }

    def openai_list_models(self) -> dict:
        """GET /v1/models 兼容格式"""
        import time as _time
        models = self.list_models()
        data = []
        for m in models:
            data.append(
                {
                    "id": m.get("model_type") or m.get("name") or "unknown",
                    "object": "model",
                    "created": int(_time.time()),
                    "owned_by": "deepseek",
                }
            )
        return {"object": "list", "data": data}

    def _iter_sse(self, session_id: str, prompt: str, model_type: str,
                  search_enabled: bool, thinking_enabled: bool,
                  parent_message_id: str | None = None):
        """
        流式 chat:内部逐行 yield 解析后的事件 dict,事件类型:
          - {"type": "ready",      "response_message_id": int}
          - {"type": "search",     "results": list[dict], "queries": list[str]}
          - {"type": "thinking",   "delta": str}
          - {"type": "text",       "delta": str}
          - {"type": "message",    "message": dict}    # 整条 message
          - {"type": "finished"}
        """
        client = self._client_with_auth()
        ch = self.get_pow_challenge("/api/v0/chat/completion")
        answer = self.solve_pow(
            ch["challenge"], ch["salt"], ch["expire_at"], ch["difficulty"]
        )
        pow_response = {
            "algorithm": ch["algorithm"],
            "challenge": ch["challenge"],
            "salt": ch["salt"],
            "answer": answer,
            "signature": ch["signature"],
            "target_path": ch["target_path"],
        }
        pow_header = base64.b64encode(json.dumps(pow_response).encode()).decode()
        body = {
            "chat_session_id": session_id,
            "parent_message_id": parent_message_id,
            "model_type": model_type,
            "prompt": prompt,
            "ref_file_ids": [],
            "thinking_enabled": thinking_enabled,
            "search_enabled": search_enabled,
            "action": None,
            "preempt": False,
        }
        headers = {**client.headers, "x-ds-pow-response": pow_header}
        with httpx.Client(headers=headers, timeout=180.0) as stream_client:
            with stream_client.stream(
                "POST",
                f"{self.BASE}/api/v0/chat/completion",
                json=body,
            ) as resp:
                if resp.status_code != 200:
                    body_text = resp.read().decode("utf-8", "ignore")
                    raise RuntimeError(
                        f"chat/completion 失败: {resp.status_code} {body_text[:300]}"
                    )
                current_event: str | None = None
                seen_results_keys: set = set()
                # 跟踪当前激活的 fragment (THINK / RESPONSE / SEARCH)
                current_fragment_type: str | None = None
                # 已通过 BATCH/yielded 出去的 THINK 初始内容(避免重复)
                emitted_think_initial: bool = False
                for line in resp.iter_lines():
                    if not line or line.startswith(":"):
                        continue
                    if line.startswith("event:"):
                        current_event = line[6:].strip()
                        continue
                    if not line.startswith("data:"):
                        continue
                    current_event = None
                    payload = line[5:].strip()
                    if not payload or payload == "[DONE]":
                        continue
                    try:
                        obj = json.loads(payload)
                    except json.JSONDecodeError:
                        continue
                    v = obj.get("v")
                    p = obj.get("p", "")
                    o = obj.get("o", "")
                    # 0) response/fragments o=APPEND  -> 切换当前 fragment 类型
                    if p == "response/fragments" and o == "APPEND" and isinstance(v, list):
                        for frag in v:
                            if not isinstance(frag, dict):
                                continue
                            new_type = frag.get("type")
                            if new_type:
                                current_fragment_type = new_type
                            # RESPONSE 初始 content 也可能是 thinking (但 type=RESPONSE 除外)
                            if new_type == "RESPONSE":
                                initial_content = frag.get("content")
                                if initial_content and isinstance(initial_content, str):
                                    yield {"type": "text", "delta": initial_content}
                        continue
                    # 1) 裸字符串 -> 文本增量 (按当前 fragment 类型)
                    if isinstance(v, str) and not p and not o:
                        if current_fragment_type == "THINK":
                            yield {"type": "thinking", "delta": v}
                        else:
                            yield {"type": "text", "delta": v}
                        continue
                    # 2) /thinking_content 片段 (兼容老格式)
                    if (
                        p.startswith("response/fragments/")
                        and p.endswith("/thinking_content")
                        and o == "APPEND"
                        and isinstance(v, str)
                    ):
                        yield {"type": "thinking", "delta": v}
                        continue
                    # 3) /content 片段: -1 表示当前激活 fragment,按类型路由
                    if (
                        p.startswith("response/fragments/")
                        and p.endswith("/content")
                        and o == "APPEND"
                        and isinstance(v, str)
                    ):
                        if current_fragment_type == "THINK":
                            yield {"type": "thinking", "delta": v}
                        else:
                            yield {"type": "text", "delta": v}
                        continue
                    # 4) 搜索结果列表
                    if p == "response/fragments/-1/results" and isinstance(v, list):
                        for r in v:
                            if isinstance(r, dict):
                                key = r.get("url") or r.get("title")
                                if key in seen_results_keys:
                                    continue
                                seen_results_keys.add(key)
                        yield {"type": "search", "results": v, "queries": []}
                        continue
                    # 5) BATCH 推送 (内含搜索 fragment)
                    if p == "response" and o == "BATCH" and isinstance(v, list):
                        results: list[dict] = []
                        queries: list[str] = []
                        for item in v:
                            if not isinstance(item, dict):
                                continue
                            # BATCH 里也可能携带 fragment 切换
                            if item.get("p") == "fragments" and item.get("o") == "APPEND" and isinstance(item.get("v"), list):
                                for frag in item["v"]:
                                    if not isinstance(frag, dict):
                                        continue
                                    if frag.get("type"):
                                        current_fragment_type = frag.get("type")
                                    if frag.get("type") == "SEARCH":
                                        for r in frag.get("results") or []:
                                            if isinstance(r, dict):
                                                key = r.get("url") or r.get("title")
                                                if key in seen_results_keys:
                                                    continue
                                                seen_results_keys.add(key)
                                                results.append(r)
                                        for q in frag.get("queries") or []:
                                            if isinstance(q, dict) and q.get("query"):
                                                queries.append(q["query"])
                        if results or queries:
                            yield {"type": "search", "results": results, "queries": queries}
                        continue
                    # 6) 初次 message 推送
                    if isinstance(v, dict) and "response" in v:
                        yield {"type": "message", "message": v["response"]}
                        # 处理初始 fragments
                        for frag in v["response"].get("fragments") or []:
                            if not isinstance(frag, dict):
                                continue
                            ftype = frag.get("type")
                            if ftype:
                                current_fragment_type = ftype
                            # 初始 THINK 内容
                            if ftype == "THINK" and isinstance(frag.get("content"), str) and frag["content"]:
                                if not emitted_think_initial:
                                    yield {"type": "thinking", "delta": frag["content"]}
                                    emitted_think_initial = True
                            # 初始 RESPONSE 内容
                            if ftype == "RESPONSE" and isinstance(frag.get("content"), str) and frag["content"]:
                                yield {"type": "text", "delta": frag["content"]}
                            # 搜索 fragment
                            if ftype == "SEARCH":
                                for r in frag.get("results") or []:
                                    if isinstance(r, dict):
                                        key = r.get("url") or r.get("title")
                                        if key in seen_results_keys:
                                            continue
                                        seen_results_keys.add(key)
                                yield {
                                    "type": "search",
                                    "results": frag.get("results") or [],
                                    "queries": [
                                        q["query"] for q in (frag.get("queries") or [])
                                        if isinstance(q, dict) and q.get("query")
                                    ],
                                }
                        continue
                    # 7) ready
                    if current_event == "ready" or (isinstance(v, dict) and "response_message_id" in v):
                        rid = None
                        if isinstance(v, dict):
                            rid = v.get("response_message_id")
                        yield {"type": "ready", "response_message_id": rid}
                        continue
                    # 8) finished
                    if p == "response/status" and o == "SET" and v == "FINISHED":
                        yield {"type": "finished"}
                        continue


    @staticmethod
    def _consume_sse(resp: httpx.Response) -> dict:
        """
        解析 DeepSeek 的 SSE 输出。
        实际事件格式:
          event: ready
          data: {"request_message_id":1, "response_message_id":2, "model_type":"default"}
          data: {"v":{"response":{"message_id":2, "role":"ASSISTANT", "content":"...实际内容..."}}}
          data: {"p":"response/fragments/-1/content","o":"APPEND","v":"！"}   # 追加文本
          data: {"v":"很高兴"}                                                    # 增量文本片段
          data: {"p":"response/status","o":"SET","v":"FINISHED"}                 # 结束
        """
        assistant_text_chunks: list[str] = []
        thinking_chunks: list[str] = []
        search_results: list[dict] = []
        search_queries: list[str] = []
        last_message: dict | None = None
        message_id: str | None = None
        title: str | None = None
        current_event: str | None = None
        for line in resp.iter_lines():
            if not line or line.startswith(":"):
                continue
            if line.startswith("event:"):
                current_event = line[6:].strip()
                continue
            current_event = None
            if not line.startswith("data:"):
                continue
            payload = line[5:].strip()
            if not payload or payload == "[DONE]":
                continue
            try:
                obj = json.loads(payload)
            except json.JSONDecodeError:
                continue
            # 0) event: title -> data: {"content":"..."}
            if current_event == "title":
                if isinstance(obj, dict) and isinstance(obj.get("content"), str):
                    title = obj["content"]
                elif isinstance(obj, str):
                    title = obj
            # 解包: 1) v 是裸字符串 -> 增量文本
            v = obj.get("v")
            p = obj.get("p", "")
            o = obj.get("o", "")
            if isinstance(v, str) and not p and not o:
                assistant_text_chunks.append(v)
                continue
            # 2) response/fragments/.../content 追加  (注意: 先判断 thinking_content,
            #    否则 endswith("/content") 会把思考片段误归到正文)
            if p.startswith("response/fragments/") and p.endswith("/thinking_content") and o == "APPEND":
                if isinstance(v, str):
                    thinking_chunks.append(v)
            elif p.startswith("response/fragments/") and p.endswith("/content") and o == "APPEND":
                if isinstance(v, str):
                    assistant_text_chunks.append(v)
            # 2b) 联网搜索结果列表 (response/fragments/-1/results)
            if p == "response/fragments/-1/results" and isinstance(v, list):
                search_results.extend(v)
            # 2d) 思考片段 (BATCH 中)
            if p == "response" and o == "BATCH" and isinstance(v, list):
                for item in v:
                    if not isinstance(item, dict):
                        continue
                    if item.get("p") == "fragments" and item.get("o") == "APPEND" and isinstance(item.get("v"), list):
                        for frag in item["v"]:
                            if isinstance(frag, dict) and frag.get("type") == "SEARCH":
                                # 搜索片段中可能直接带 results
                                if isinstance(frag.get("results"), list):
                                    search_results.extend(frag["results"])
                                if isinstance(frag.get("queries"), list):
                                    for q in frag["queries"]:
                                        if isinstance(q, dict) and q.get("query"):
                                            search_queries.append(q["query"])
            # 3) 完整 message 内容 (初次发送)
            if p == "response" and o == "BATCH":
                # 在 BATCH 中可能含 content
                pass
            # 4) v 是 dict 且含 response 字段
            if isinstance(v, dict) and "response" in v:
                last_message = v["response"]
                message_id = last_message.get("message_id")
                # 初次 message 内也可能有搜索结果
                frags = last_message.get("fragments") or []
                for frag in frags:
                    if not isinstance(frag, dict):
                        continue
                    if isinstance(frag.get("results"), list):
                        search_results.extend(frag["results"])
                    if isinstance(frag.get("queries"), list):
                        for q in frag["queries"]:
                            if isinstance(q, dict) and q.get("query"):
                                search_queries.append(q["query"])
            # 5) title 事件
            if "title" in obj and isinstance(obj.get("title"), dict):
                title = obj["title"].get("content")
            # 6) 搜索结果: search_result / web_search
            if "search_results" in obj and isinstance(obj["search_results"], list):
                search_results.extend(obj["search_results"])
            if "web_search" in obj and isinstance(obj["web_search"], dict):
                ws = obj["web_search"]
                if ws.get("results"):
                    search_results.extend(ws["results"])
            # 7) 状态结束
            if p == "response/status" and o == "SET" and v == "FINISHED":
                pass
        return {
            "text": "".join(assistant_text_chunks),
            "thinking": "".join(thinking_chunks),
            "search_results": search_results,
            "search_queries": search_queries,
            "message_id": message_id,
            "title": title,
            "last_message": last_message,
        }


